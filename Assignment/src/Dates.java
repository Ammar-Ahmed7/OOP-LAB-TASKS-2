public class Dates {

    private int date;
    private int month;
    private int year;



    public void setDate(int d)
    {
        date=d;
    }
    public int getDate()
    {
        return date;
    }



    public void setMonths(int m)
    {
        month=m;
    }
    public int getMonths()
    {
       return month;
    }



    public void setYear(int y)
    {
        year=y;
    }
    public int getYear()
    {
        return year;
    }

}
